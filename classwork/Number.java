import java.util.Scanner;
public class Number {
public static void main(String[] args) {
 Scanner input = new Scanner(System.in);
System.out.print("Enter integer from 1 and 5:");
int number = input.nextInt();

if (number == 1){
number = 1 + 0;
System.out.println("The square of number is:" + number);
} else if (number == 2){
 number  = 2 + 2;
System.out.println("The square of number is:" + number);
} else if (number == 3){
 number  = 3 + 3 + 3;
System.out.println("The square of number is:" + number);
} else if (number == 4){
 number  = 4 + 4 + 4 + 4;
System.out.println("The square of number is:" + number);
} else if (number == 5){
 number  = 5 + 5 + 5 + 5 + 5;
System.out.println("The square of number is:" + number);
}
}
}


 



